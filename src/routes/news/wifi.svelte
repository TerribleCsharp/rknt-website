		<h1>Festiwal WiFI</h1>
		<h2>3 września</h2>
	</header>
	<p>
		Już 13 października RKNT Invenio wystąpi na Wiśniowym Festiwalu Inicjatyw “WiFI”! WiFI to
		coroczne wydarzenie, organizowane przez Samorząd Uczniowski Zespółu Szkół Licealnych i
		Technicznych nr 1 w Warszawie “Wiśniowa”, na którym uczniowie szkół średnich, mogą prezentować
		swoje pasje i zainteresowania, w tym techniczne i naukowe projekty. Na festiwal zapraszani są
		też przedstawiciele firm branży technologicznej, dzięki czemu stanowi on świetną okazję do
		nawiązania wielu współprac i gwarantuje możliwość dotarcia ze swoim pomysłem do profesjonalistów
		pracujących w danej dziedzinie od lat. Na swojej wystawie, RKNT Invenio będzie prezentować
		efekty swoich dotychczasowych prac - przede wszystkim model 1:1 silnika Bieli3K umieszczony na
		ukończonej już hamowni, oraz niezbędny do jego funkcjonowania komputer pokładowy. Pokażemy
		również poprzednie projekty naszych członków, m.in. rakietę Vector oraz łazika i robota,
		zdolnego do układania kostki rubika oraz przedstawimy nasz plany rozwoju na przyszłość. Aby
		dowiedzieć się więcej, wejdź <a href="https://wisniowasu.pl/wifi/">na stronę WiFI</a>.
	</p>
	<h2>13 października</h2>
	<p>
		Ze względu na pandemię, festiwal WiFI nie był dostępny dla zainteresowanych osób spoza ZSLiT
		“Wiśniowa”. Mimo to, RKNT wzięło w nim udział, a nasza wystawa, cieszyła się największą
		popularnością ze wszystkich, odnotowując ponad 200 odwiedzających. Bardzo cieszy nas to
		zainteresowanie, a wszystkich, którzy nie byli w stanie uczestniczyć w festiwalu, zapraszamy na
		przyszłoroczną edycję, na której z pewnością również nas nie zabraknie!
	</p>

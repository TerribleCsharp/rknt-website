		<h1>RKNT bierze udział w ZZT</h1>
		<h2>2 grudnia</h2>
		<p>
			Rakietowe Koło Naukowo-Techniczne “Invenio” bierze udział w olimpiadzie “Zwolnieni z Teorii”!
			“Zwolnieni z Teorii” to ogólnopolski konkurs, którego głównym celem, jest promowanie i
			popularyzowanie przedsiębiorczych i innowacyjnych postaw wśród młodzieży szkolnej. Nasz zespół
			zdecydował się na dołączenie do projektu, dzięki czemu. oprócz szansy na walkę o tytuł
			laureata tej olimpiady, otrzymał także profesjonalne wsparcie od mentora, wspomagającego nas
			od dzisiaj w realizacji naszych planów, a także możliwość współpracy z największymi firmami
			działającymi w Polsce, spośród których zdecydowaliśmy się na ING Bank Śląski. Mamy nadzieje,
			że owoce tych współprac, będziecie mogli zobaczyć na naszych social mediach już niedługo!

	<p>
		MEWAM (Mechaniczno Elektroniczne Wspomaganie Aktywności Mięśni) to projekt budowy mechanicznego
		egzoszkieletu, który będzie w stanie poruszać ręką noszącej go osoby. Cały mechanizm działa na
		podstawie własnoręcznie zaprojektowanych detektorów napięcia mięśniowego, które następnie
		przekazują sygnał do zespołu silników wprawiających w ruch przymocowane do dłoni szyny.
	</p>
	<p>
		Projekt MEWAM powstał z myślą o osobach z niedowładem kończyn, częściowymi paraliżami i innymi
		chorobami układu ruchu, które w znacznym stopniu utrudniają codzienne funkcjonowanie. Docelowo,
		zbudowany przez nas egzoszkielet będzie mógł stanowić przydatne narzędzie w terapii takich osób
		i przywracania im dawnej sprawności, a niewielka waga całego urządzenia, która jest jedną z cech
		wyróżniających je na tle dotychczasowych rozwiązań, pozwoli na korzystanie z niego dłużej i w
		bardziej komfortowych warunkach, nie przeciążając niepotrzebnie organizmu.
	</p>
	<p>
		Projekt MEWAM jest patronowany przez RKNT Invenio. Obecnie nad nim pracuje niewielki w
		porównaniu do pozostałych inicjatyw, kilkuosobowy zespół. Tym bardziej imponuje jest prędkość z
		jaką udało osiągnąć się pierwsze wymierne rezultaty - zaledwie w kilka miesięcy od rozpoczęcia
		budowy egzoszkieletu udało się ukończyć jego pierwszą, prototypową wersję, która jest obecnie
		usprawniana i udoskonalana. Jeśli interesuje Was rozwój projektu MEWAM dołączajcie na naszego
		<a href="https://discord.gg/uHaQarJAEu">discorda</a> gdzie ekskluzywnie udostępniamy treści
		dotyczące naszego mechanicznego ramienia i oglądajcie
